/**
 * Get a subject name from its code. Returns the code itself if not found.
 */
export function getSubjectName(code: string): string {
  const clean = code.replace(/\s*\(T\)\s*|\s*\(P\)\s*/g, "").trim();
  return SUBJECT_NAMES[clean] || clean;
}

/**
 * Parse a referred subject string like "25913(T), 25921(T)" into an array of
 * { code, name, type } objects.
 */
export function parseReferredSubjects(refSubStr: string): Array<{ code: string; name: string; type: string }> {
  const parts = refSubStr.split(/[,;]/).map((s) => s.trim()).filter(Boolean);
  return parts.map((part) => {
    const m = part.match(/^(\d{4,6})\s*\(([TP])\)$/);
    if (m) {
      return { code: m[1], name: getSubjectName(m[1]), type: m[2] === "T" ? "Theory" : "Practical" };
    }
    const codeMatch = part.match(/^(\d{4,6})/);
    if (codeMatch) {
      return { code: codeMatch[1], name: getSubjectName(codeMatch[1]), type: "Theory" };
    }
    return { code: part, name: part, type: "Theory" };
  });
}
